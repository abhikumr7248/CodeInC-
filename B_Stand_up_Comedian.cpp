/*------------------------------------------------------------------------------------------------------------------------------*/
void solve()
{
    int a, b, c, d;
    cin >> a >> b >> c >> d;
    int ans = a;
    if (a!=0)
    {
        int up = min(b, c);
        ans += up * 2;
        b -= up;
        c -= up;
    }

    int p = b + c + d;
    ans += min(a + 1, p);

    cout << ans <<endl;
}
/*------------------------------------------------------------------------------------------------------------------------------*/
int main(void)
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int n;
    cin >> n;
    for (int i = 0; i < n; ++i)
        solve();
    return 0;
}